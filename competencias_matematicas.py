def get_competencia_by_id(competencia_id: str):
    """Busca una competencia específica por su ID"""
    for grado, competencias in COMPETENCIAS_MATEMATICAS.items():
        for comp in competencias:
            if comp['id'] == competencia_id:
                return comp
    return None
